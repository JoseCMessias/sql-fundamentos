-- SELECT FROM
-- SELECT AS
-- SELECT LIMIT
-- ORDER BY

-- SELECT FROM - O Objetivo é selecionar todas as colunas e todas as linhas de uma tabela.
-- Exemplo: SELECT * FROM Tabela;
-- SELECT (Opção 2) - O Objetivo é selecionar apenas colunas específicas de uma tabela.
-- Exemplo: SELECT Col1, Col2 FROM Tabela; 

-- SELECT AS - O objetivo é selecionar colunas especificas e dar um nome para essas colunas.
/* Exemplo: SELECT 
                Col1 AS "Coluna 1",
                Col2 AS "Coluna 2"
			FROM Tabela;
*/                

-- SELECT LIMIT - O objetivo é selecionar apenas as N primeiras linhas de uma determinada tabela.
/* Exemplo: SELECT 
				*
			FROM Tabela
            LIMIT 2;
*/

-- ORDER BY (ASC - Ascendente / DESC - Descendente) - Permite Ordemanr (classificar) uma tabela a partir de uma determinada coluna.
-- ORDER BY (ASC)
/* Exemplo: SELECT 
			    *
			FROM Tabela
            ORDER BY Col3;
*/
-- ORDER BY (DESC)
/* Exemplo: SELECT 
			    *
			FROM Tabela
            ORDER BY Col3 DESC;
*/








